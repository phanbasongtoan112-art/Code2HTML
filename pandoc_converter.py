def upgrade_doc_to_docx(doc_path: Path, tmp_dir: Path) -> Path:
    """
    Convert a legacy binary .doc file to .docx format so that mammoth (which
    is a ZIP/OOXML parser) can process it.

    Why this is necessary
    ---------------------
    The legacy .doc format is a binary OLE Compound File.  mammoth only
    understands OOXML (.docx, which is a ZIP archive).  Passing a .doc file
    to mammoth raises ``zipfile.BadZipFile`` immediately.  This function
    bridges that gap by re-saving the document as .docx using whichever
    tool is available on the current machine.

    Conversion strategies (tried in order)
    ---------------------------------------
    1. **Word COM** (Windows + Microsoft Word installed)
       Opens the file with ``win32com.client`` and calls ``SaveAs2`` with
       ``wdFormatXMLDocument`` (format code 12), which writes a proper .docx.

    2. **LibreOffice headless**
       Calls ``soffice --headless --convert-to docx`` which uses LibreOffice's
       built-in Word filter for the re-save.

    Args:
        doc_path: Absolute path to the source .doc file.
        tmp_dir:  A temporary directory where the .docx will be written.

    Returns:
        Path to the newly created .docx file inside *tmp_dir*.

    Raises:
        RuntimeError: if neither Word COM nor LibreOffice is available, or if
                      the conversion subprocess/COM call fails.
    """
    docx_path = tmp_dir / (doc_path.stem + ".docx")

    # ── Strategy 1: Word COM ─────────────────────────────────────────────────
    try:
        import win32com.client as win32
        step("Upgrading .doc → .docx via Microsoft Word COM …")
        word = None
        try:
            word = win32.gencache.EnsureDispatch("Word.Application")
            word.Visible = False
            word.DisplayAlerts = False
            doc = word.Documents.Open(str(doc_path.resolve()))
            WD_FORMAT_XML_DOCUMENT = 12   # .docx
            doc.SaveAs2(str(docx_path.resolve()), FileFormat=WD_FORMAT_XML_DOCUMENT)
            doc.Close(False)
            ok(f"Upgraded → {docx_path.name}")
            return docx_path
        finally:
            if word is not None:
                try:
                    word.Quit()
                except Exception:
                    pass
    except ImportError:
        pass  # pywin32 not installed, try next strategy

    # ── Strategy 2: LibreOffice headless ────────────────────────────────────
    soffice = shutil.which("soffice") or shutil.which("libreoffice")
    if soffice:
        step("Upgrading .doc → .docx via LibreOffice …")
        # Copy input into tmp_dir first (LibreOffice writes output next to source)
        tmp_doc = tmp_dir / doc_path.name
        shutil.copy2(doc_path, tmp_doc)
        cmd = [soffice, "--headless", "--convert-to", "docx", "--outdir",
               str(tmp_dir), str(tmp_doc)]
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=120)
        if result.returncode != 0:
            raise RuntimeError(
                f"LibreOffice .doc→.docx upgrade failed (exit {result.returncode}).\n"
                f"stderr: {result.stderr}"
            )
        candidates = list(tmp_dir.glob("*.docx"))
        if not candidates:
            raise RuntimeError("LibreOffice ran but produced no .docx file.")
        ok(f"Upgraded → {candidates[0].name}")
        return candidates[0]

    # ── No strategy available ────────────────────────────────────────────────
    raise RuntimeError(
        "Cannot convert legacy .doc file: no suitable converter found.\n\n"
        "To fix this on Windows, install ONE of the following:\n\n"
        "  Option A – Pandoc (recommended, free, no Office required):\n"
        "    winget install --id JohnMacFarlane.Pandoc\n"
        "    or download from: https://pandoc.org/installing.html\n"
        "    then re-run:  pip install pypandoc\n\n"
        "  Option B – LibreOffice (free, no Office required):\n"
        "    winget install --id TheDocumentFoundation.LibreOffice\n"
        "    or download from: https://www.libreoffice.org/download/\n\n"
        "  Option C – pywin32 (requires Microsoft Word to be installed):\n"
        "    pip install pywin32\n\n"
        "After installing, re-run the script — no code changes needed."
    )


