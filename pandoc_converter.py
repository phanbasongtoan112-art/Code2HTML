def convert_doc_pandoc(input_path: Path, output_path: Path) -> None:
    """
    Convert a Word document (.doc or .docx) to HTML using pypandoc / Pandoc.

    Pandoc is a universal document converter that understands both legacy
    .doc (via LibreOffice filter internally) and modern .docx.  Using
    --standalone produces a complete HTML file; --embed-resources embeds
    all images as base64 (Pandoc ≥ 2.19 replaces --self-contained).

    Args:
        input_path:  Path to the source .doc or .docx file.
        output_path: Path where the .html file should be written.

    Raises:
        ImportError:   if pypandoc is not installed.
        RuntimeError:  if Pandoc binary is not found or conversion fails.
    """
    try:
        import pypandoc
    except ImportError:
        raise ImportError(
            "pypandoc is required for .doc conversion.\n"
            "Install it with:  pip install pypandoc\n"
            "Then install Pandoc: https://pandoc.org/installing.html"
        )

    step(f"Converting '{input_path.name}' using Pandoc …")

    # Build extra args; try --embed-resources (Pandoc ≥2.19) with fallback
    # to the older --self-contained flag.
    def _run(embed_flag: str) -> str:
        return pypandoc.convert_file(
            str(input_path),
            "html5",
            outputfile=None,           # return as string
            extra_args=[
                "--standalone",
                embed_flag,
                "--metadata", f"title={input_path.stem}",
                "--highlight-style=tango",
            ],
        )

    try:
        raw_html = _run("--embed-resources")
    except Exception:
        step("--embed-resources not supported, retrying with --self-contained …")
        try:
            raw_html = _run("--self-contained")
        except Exception as exc:
            raise RuntimeError(
                f"Pandoc conversion failed: {exc}\n"
                "Make sure Pandoc ≥ 2.11 is installed: https://pandoc.org/installing.html"
            ) from exc

    # Inject our own CSS before Pandoc's closing </style> tag (or <body>)
    injection = f"\n<style>\n{BASE_CSS}\n</style>\n"
    if "</head>" in raw_html:
        raw_html = raw_html.replace("</head>", injection + "</head>", 1)
    else:
        raw_html = injection + raw_html

    output_path.write_text(raw_html, encoding="utf-8")
    ok(f"Saved → {output_path}")


