def _mammoth_embed_image(image) -> dict:
    """
    Callback for mammoth's convert_image hook.

    mammoth passes an image object whose .open() method is a context manager
    (returns a contextlib.closing-wrapped file).  We must use 'with' to read
    the bytes safely — calling .open().read() directly fails because the
    'closing' wrapper has no .read() attribute.
    """
    with image.open() as img_file:
        data = img_file.read()
    b64 = base64.b64encode(data).decode()
    return {"src": f"data:{image.content_type};base64,{b64}"}


def convert_docx_mammoth(input_path: Path, output_path: Path) -> None:
    """
    Convert a .docx file to a standalone HTML file using the mammoth library.

    mammoth is purpose-built for Word→HTML and handles:
      • Headings, bold, italic, underline, strikethrough
      • Tables (including merged cells via rowspan/colspan)
      • Lists (nested)
      • Hyperlinks
      • Images (embedded as base64 automatically by mammoth)
      • Custom style mappings via style_map

    Args:
        input_path:  Path to the source .docx file.
        output_path: Path where the .html file should be written.

    Raises:
        ImportError:   if mammoth is not installed.
        FileNotFoundError: if the input file does not exist.
        RuntimeError:  if mammoth reports conversion errors.
    """
    try:
        import mammoth
    except ImportError:
        raise ImportError(
            "mammoth is required for .docx conversion.\n"
            "Install it with:  pip install mammoth"
        )

    step(f"Converting '{input_path.name}' using mammoth …")

    with input_path.open("rb") as fh:
        result = mammoth.convert_to_html(
            fh,
            style_map=MAMMOTH_STYLE_MAP,
            # Embed images as base64 data URIs (no external files needed)
            convert_image=mammoth.images.img_element(_mammoth_embed_image),
        )

    # Report any warnings mammoth emitted (e.g. unsupported features)
    for msg in result.messages:
        if "error" in str(msg.type).lower():
            warn(f"mammoth: {msg.message}")
        else:
            step(f"mammoth note: {msg.message}")

    body_html = result.value
    title = input_path.stem
    full_html = _wrap_html(title, body_html, MAMMOTH_EXTRA_CSS)

    output_path.write_text(full_html, encoding="utf-8")
    ok(f"Saved → {output_path}")


