def convert_to_html(
    input_path: str | Path,
    output_path: str | Path | None = None,
    *,
    prefer_pandoc: bool = False,
) -> Path:
    """
    Main entry point: convert *input_path* to an HTML file.

    Automatically chooses the best available converter:
      .docx  →  mammoth  (unless prefer_pandoc=True and Pandoc is available)
      .doc   →  pypandoc → LibreOffice → Word COM (Windows)

    Args:
        input_path:    Path to the Word document (.doc or .docx).
        output_path:   Optional desired output path.  If None, the output
                       is placed beside the input with a .html extension.
        prefer_pandoc: If True, use Pandoc even for .docx files (useful
                       when you want Pandoc's richer metadata handling).

    Returns:
        The Path of the generated HTML file.

    Raises:
        FileNotFoundError: if the input file does not exist.
        ValueError:        if the file extension is not .doc or .docx.
        RuntimeError:      if no suitable converter is found.
    """
    input_path = Path(input_path).resolve()

    # ── Validate input ──────────────────────────────────────────────────────
    if not input_path.exists():
        raise FileNotFoundError(f"Input file not found: {input_path}")

    ext = input_path.suffix.lower()
    if ext not in (".doc", ".docx"):
        raise ValueError(
            f"Unsupported file extension '{ext}'. "
            "Only .doc and .docx are accepted."
        )

    # ── Resolve output path ─────────────────────────────────────────────────
    if output_path is None:
        output_path = input_path.with_suffix(".html")
    else:
        output_path = Path(output_path).resolve()
        # Ensure the target directory exists
        output_path.parent.mkdir(parents=True, exist_ok=True)

    info(f"Input  : {input_path}")
    info(f"Output : {output_path}")

    deps = check_dependencies()
    _print_dep_summary(deps)
    print()

    # ── .docx branch ────────────────────────────────────────────────────────
    if ext == ".docx":
        if not prefer_pandoc and deps["mammoth"]:
            info("Strategy: mammoth (native Python, .docx)")
            convert_docx_mammoth(input_path, output_path)

        elif deps["pypandoc"] and deps["pandoc"]:
            info("Strategy: Pandoc via pypandoc (.docx)")
            convert_doc_pandoc(input_path, output_path)

        elif deps["libreoffice"]:
            info("Strategy: LibreOffice headless (.docx)")
            convert_via_libreoffice(input_path, output_path)

        elif deps["win32com"]:
            info("Strategy: Microsoft Word COM (.docx)")
            convert_via_word_com(input_path, output_path)

        else:
            raise RuntimeError(
                "No converter found for .docx files!\n\n"
                "Install at least one of:\n"
                "  pip install mammoth            # recommended\n"
                "  pip install pypandoc           # + Pandoc binary\n"
                "  sudo apt install libreoffice   # Linux\n"
                "  brew install --cask libreoffice  # macOS"
            )

    # ── .doc branch ─────────────────────────────────────────────────────────
    else:  # ext == ".doc"
        if deps["pypandoc"] and deps["pandoc"]:
            info("Strategy: Pandoc via pypandoc (.doc)")
            convert_doc_pandoc(input_path, output_path)

        elif deps["libreoffice"]:
            info("Strategy: LibreOffice headless (.doc)")
            convert_via_libreoffice(input_path, output_path)

        elif deps["win32com"]:
            info("Strategy: Microsoft Word COM (.doc)")
            convert_via_word_com(input_path, output_path)

        elif deps["mammoth"]:
            # mammoth cannot read binary .doc directly (it is an OLE file,
            # not a ZIP).  However we can upgrade .doc → .docx first using
            # Word COM or LibreOffice, then feed the .docx to mammoth.
            info("Strategy: upgrade .doc → .docx, then mammoth")
            with tempfile.TemporaryDirectory() as tmp:
                docx_path = upgrade_doc_to_docx(input_path, Path(tmp))
                convert_docx_mammoth(docx_path, output_path)

        else:
            raise RuntimeError(
                "No converter found for .doc files!\n\n"
                "To fix this on Windows, install ONE of the following:\n\n"
                "  Option A – Pandoc (recommended, free, no Office required):\n"
                "    winget install --id JohnMacFarlane.Pandoc\n"
                "    or download from: https://pandoc.org/installing.html\n"
                "    then re-run:  pip install pypandoc\n\n"
                "  Option B – LibreOffice (free, no Office required):\n"
                "    winget install --id TheDocumentFoundation.LibreOffice\n"
                "    or download from: https://www.libreoffice.org/download/\n\n"
                "  Option C – pywin32 (requires Microsoft Word to be installed):\n"
                "    pip install pywin32\n\n"
                "After installing, re-run the script — no code changes needed."
            )

    return output_path


# ---------------------------------------------------------------------------
# CLI entry point
# ---------------------------------------------------------------------------

def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="doc2html",
        description=(
            "Universal DOC/DOCX → HTML converter.\n"
            "Supports mammoth, Pandoc, LibreOffice, and Word COM (Windows)."
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples:\n"
            "  python doc2html.py report.docx\n"
            "  python doc2html.py report.docx output.html\n"
            "  python doc2html.py legacy.doc --prefer-pandoc\n"
            "  python doc2html.py --check-deps\n"
        ),
    )
    parser.add_argument(
        "input",
        nargs="?",
        help="Path to the input .doc or .docx file.",
    )
    parser.add_argument(
        "output",
        nargs="?",
        default=None,
        help="Optional path for the output .html file (default: same dir as input).",
    )
    parser.add_argument(
        "--prefer-pandoc",
        action="store_true",
        default=False,
        help="Use Pandoc instead of mammoth even for .docx files.",
    )
    parser.add_argument(
        "--check-deps",
        action="store_true",
        default=False,
        help="Print dependency status and exit.",
    )
    return parser


def main(argv: list[str] | None = None) -> int:
    """
    Parse CLI arguments and run the conversion.

    Returns:
        0 on success, 1 on error.
    """
    parser = _build_parser()
    args   = parser.parse_args(argv)

    # ── --check-deps mode ───────────────────────────────────────────────────
    if args.check_deps:
        deps = check_dependencies()
        _print_dep_summary(deps)
        return 0

    # ── Normal conversion mode ──────────────────────────────────────────────
    if not args.input:
        parser.print_help()
        return 1

    try:
        result = convert_to_html(
            args.input,
            args.output,
            prefer_pandoc=args.prefer_pandoc,
        )
        print()
        ok(f"Conversion complete!  Open in browser: {result}")
        return 0

    except FileNotFoundError as exc:
        error(str(exc))
        return 1

    except ValueError as exc:
        error(str(exc))
        return 1

    except ImportError as exc:
        error(str(exc))
        return 1

    except RuntimeError as exc:
        error(str(exc))
        return 1

    except KeyboardInterrupt:
        print()
        warn("Interrupted by user.")
        return 130


if __name__ == "__main__":
    sys.exit(main())
