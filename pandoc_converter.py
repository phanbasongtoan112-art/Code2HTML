def convert_via_word_com(input_path: Path, output_path: Path) -> None:
    """
    Convert a Word document to HTML via Word's COM automation (Windows only).

    This approach launches Microsoft Word invisibly and uses its built-in
    "Save As HTML" export, producing the highest-fidelity output at the cost
    of requiring a licensed Microsoft Word installation.

    Uses wdFormatHTML (8) for compatibility or wdFormatFilteredHTML (10) for
    a cleaner, lighter-weight output.

    Args:
        input_path:  Absolute path to the source document.
        output_path: Desired path for the resulting .html file.

    Raises:
        ImportError:   if pywin32 is not installed.
        RuntimeError:  if Word cannot be launched or conversion fails.
    """
    try:
        import win32com.client as win32
    except ImportError:
        raise ImportError(
            "pywin32 is required for Word COM conversion.\n"
            "Install it with:  pip install pywin32"
        )

    step(f"Converting '{input_path.name}' using Microsoft Word COM …")

    word = None
    doc  = None
    try:
        word = win32.gencache.EnsureDispatch("Word.Application")
        word.Visible = False
        word.DisplayAlerts = False

        doc = word.Documents.Open(str(input_path.resolve()))

        # wdFormatFilteredHTML = 10  (cleaner HTML, recommended)
        # wdFormatHTML         = 8   (preserves more Word-specific markup)
        WD_FORMAT_FILTERED_HTML = 10
        doc.SaveAs2(str(output_path.resolve()), FileFormat=WD_FORMAT_FILTERED_HTML)
        doc.Close(False)

        # Word writes supporting images into a folder beside the HTML;
        # re-read the HTML and inline those images as base64.
        html = output_path.read_text(encoding="utf-8", errors="replace")
        html = _embed_images_as_base64(html, output_path.parent)

        # Inject base CSS
        injection = f"\n<style>\n{BASE_CSS}\n</style>\n"
        if "</head>" in html:
            html = html.replace("</head>", injection + "</head>", 1)
        html_path = output_path.with_suffix(".html")
        html_path.write_text(html, encoding="utf-8")

    except Exception as exc:
        raise RuntimeError(f"Word COM conversion failed: {exc}") from exc
    finally:
        # Always quit Word to avoid ghost processes
        if word is not None:
            try:
                word.Quit()
            except Exception:
                pass

    ok(f"Saved → {output_path}")


