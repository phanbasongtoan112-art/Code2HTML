def _escape_html(text: str) -> str:
    return (
        text.replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace('"', "&quot;")
    )


def _embed_images_as_base64(html: str, base_dir: Path) -> str:
    """
    Replace <img src="relative/path"> with inline base64 data URIs so the
    HTML file is truly self-contained.
    """
    MIME = {
        ".png": "image/png", ".jpg": "image/jpeg", ".jpeg": "image/jpeg",
        ".gif": "image/gif", ".bmp": "image/bmp", ".svg": "image/svg+xml",
        ".webp": "image/webp",
    }

    def replacer(m: re.Match) -> str:
        prefix, src, suffix = m.group(1), m.group(2), m.group(3)
        # Skip already-embedded or remote images
        if src.startswith("data:") or src.startswith("http"):
            return m.group(0)
        img_path = (base_dir / src).resolve()
        if not img_path.exists():
            warn(f"Image not found, skipping embed: {img_path}")
            return m.group(0)
        ext = img_path.suffix.lower()
        mime = MIME.get(ext, "application/octet-stream")
        b64 = base64.b64encode(img_path.read_bytes()).decode()
        return f'{prefix}data:{mime};base64,{b64}{suffix}'

    # Match both single- and double-quoted src attributes
    pattern = r'(<img\s[^>]*src=")([^"]+)(")'
    html = re.sub(pattern, replacer, html, flags=re.IGNORECASE)
    pattern2 = r"(<img\s[^>]*src=')([^']+)(')"
    html = re.sub(pattern2, replacer, html, flags=re.IGNORECASE)
    return html


