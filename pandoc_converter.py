def convert_via_libreoffice(input_path: Path, output_path: Path) -> None:
    """
    Convert a Word document to HTML using LibreOffice in headless mode.

    LibreOffice is called as a subprocess with the --headless flag so no
    GUI window ever appears.  It writes the HTML alongside the source file
    in a temp directory, after which we move it to the desired output path.

    LibreOffice binary name differs per platform:
        Linux/macOS: soffice  or  libreoffice
        Windows:     "C:\\Program Files\\LibreOffice\\program\\soffice.exe"
                     (added to PATH by the installer on most systems)

    Args:
        input_path:  Path to the source document.
        output_path: Desired path for the resulting .html file.

    Raises:
        RuntimeError: if LibreOffice is not found or conversion fails.
    """
    soffice = shutil.which("soffice") or shutil.which("libreoffice")
    if soffice is None:
        raise RuntimeError(
            "LibreOffice not found on PATH.\n"
            "Install from: https://www.libreoffice.org/download/download/\n"
            "On Ubuntu/Debian:  sudo apt install libreoffice\n"
            "On macOS (Homebrew): brew install --cask libreoffice"
        )

    step(f"Converting '{input_path.name}' using LibreOffice …")

    # LibreOffice writes output next to the source file, so we use a temp dir.
    with tempfile.TemporaryDirectory() as tmp_dir:
        tmp_input = Path(tmp_dir) / input_path.name
        # Copy input into temp dir (LibreOffice may lock the original)
        shutil.copy2(input_path, tmp_input)

        cmd = [
            soffice,
            "--headless",
            "--convert-to", "html:HTML",
            "--outdir", tmp_dir,
            str(tmp_input),
        ]
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=120)

        if result.returncode != 0:
            raise RuntimeError(
                f"LibreOffice returned exit code {result.returncode}.\n"
                f"stdout: {result.stdout}\nstderr: {result.stderr}"
            )

        # Find the generated HTML file
        generated = list(Path(tmp_dir).glob("*.html"))
        if not generated:
            raise RuntimeError(
                "LibreOffice ran but produced no .html output.\n"
                f"stdout: {result.stdout}\nstderr: {result.stderr}"
            )

        lo_html = generated[0].read_text(encoding="utf-8", errors="replace")

        # Embed any <img> tags that point to local files LibreOffice created
        lo_html = _embed_images_as_base64(lo_html, Path(tmp_dir))

        # Inject base CSS
        injection = f"\n<style>\n{BASE_CSS}\n</style>\n"
        if "</head>" in lo_html:
            lo_html = lo_html.replace("</head>", injection + "</head>", 1)
        else:
            lo_html = injection + lo_html

    output_path.write_text(lo_html, encoding="utf-8")
    ok(f"Saved → {output_path}")


